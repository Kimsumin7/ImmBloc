package tommy.java.exam17;

public class CellPhone {
	String model;
	String number;
	int chord;

	public void setNumber(String n) {
		number = n;
	}

	public String getModel() {
		return model;
	}

	public int getChord() {
		return chord;
	}

	public String getNumber() {
		return number;
	}
}
